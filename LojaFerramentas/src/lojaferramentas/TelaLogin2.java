    private void Logar(Usuario novo){
        this.conectar.conectaBanco();
        //String senhaCorreta="";
        String consultaemail = this.txtemail.getText();
        
        try {
            this.conectar.executarSQL(
                "SELECT  "       
                +"senha"
             + " FROM "
                 + "Usuario"
             + " WHERE "
                 + "email = '" + consultaemail + "'" + ";"       
            );
        while(this.conectar.getResultSet().next()){
            novo.setSenha(this.conectar.getResultSet().getString(1));
           
        }
            
        
        } catch (Exception e){
            System.out.println("Erro ao consultar cliente " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao buscar cliente");
        } finally {
            teste.setText(novo.getSenha());
            if (teste.getText().equals(txtsenha.getText())) 
                //Passa pra tela de consulta
                JOptionPane.showMessageDialog(null, "Senha correta");
                Cadastro_Prestador tela = new Cadastro_Prestador();
                dispose(); // fecha janela atual
                tela.setVisible(true); 
            
        }
        
    }

