    private void Logar(Usuario novo){
        this.conectar.conectaBanco();
        
        String consultaemail = this.txtemail.getText();
        
        try {
            this.conectar.executarSQL(
                "SELECT  "       
                +"senha"
             + " FROM "
                 + "Usuarios"
             + " WHERE "
                 + "email = '" + consultaemail + "'" + ";"       
            );
        
            senhaCorreta = this.conectar.getResultSet().getString(1);
        } catch (Exception e){
            System.out.println("Erro ao consultar cliente " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao buscar cliente");
        } finally {
            if (senhaCorreta.equals(txtsenha.getText())) {
                //Passa pra tela de consulta
                JOptionPane.showMessageDialog(null, "Senha correta");
            }
        }
        
    }

